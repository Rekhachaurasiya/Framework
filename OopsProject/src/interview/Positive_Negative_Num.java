package interview;

public class Positive_Negative_Num {
	public static void main(String[] args) {
		double number=-90.87;
		if(number<0.0)
		{
			System.out.println("Negative number:"+number);
		}
		else if(number>0.0)
		{
			System.out.println("Positive numbers:"+number);
		}
		else
		{
			System.out.println("number is zero:"+number);
		}
	}
	

}
